VOICES_PATH = "sounds/voices"


def bytes_to_int(b):
    return int.from_bytes(b, byteorder="little")


def reverse_dict(input_dict: dict):
    reversed_dict = {}
    for key, value in input_dict.items():
        if type(value) is dict:
            inner = reverse_dict(value)
            for inner_key, inner_value in inner.items():
                if inner_key not in reversed_dict:
                    reversed_dict[inner_key] = inner_value
                reversed_dict[inner_key].insert(0, key)
        elif type(value) is list:
            for element in value:
                if type(element) is dict:
                    inner = reverse_dict(value)
                if element not in reversed_dict:
                    reversed_dict[element] = []
                reversed_dict[element].append(key)
        else:
            if value not in reversed_dict:
                reversed_dict[value] = []
            reversed_dict[value].append(key)
    return reversed_dict


def extract_voiceover_files(voices_loc, voices, output_dir):
    # Yeah, I know that I can just store reversed
    # voices dict but this file is already 2111 lines
    dirs = reverse_dict(voices)

    with open(voices_loc, "rb") as packed:
        magic = packed.read(4)

        if not magic == b"BFPK":
            raise ValueError(f"oopsie doopsie, {voices_loc} is fucky whacky")

        _ = bytes_to_int(packed.read(4))  # version skipped
        files_count = bytes_to_int(packed.read(4))
        files = {}

        for i in range(0, files_count):
            path_length = bytes_to_int(packed.read(4))
            path = packed.read(path_length).decode('latin1')
            data_size = bytes_to_int(packed.read(4))
            data_offset = bytes_to_int(packed.read(4))

            if path.startswith(VOICES_PATH):
                files[path[len(VOICES_PATH):]] = {"size": data_size, "offset": data_offset}

        total_files = len(dirs)
        current_file = 1
        for file, data in files.items():
            relative_outpath = f"{output_dir}/{'/'.join(dirs[file])}"
            outpath = os.path.abspath(relative_outpath)
            outfilepath = f"{outpath}/{file}"
            files_count = f"{total_files}/{current_file}"
            stats = f"offset: {data['offset']}, size: {data['size']}"

            if not os.path.exists(outpath):
                os.makedirs(outpath)

            with open(outfilepath, "wb") as out:
                print(f"[{files_count}] Exctracting to {relative_outpath} ({stats})")
                packed.seek(data["offset"])
                out.write(packed.read(data["size"]))

            current_file += 1


def copy_voiceover_files(voices_loc, input, output_dir, depth=0):
    if type(input) is dict:
        for key, val in input.items():
            new_out = os.path.abspath(output_dir + '/' + key)
            if not os.path.exists(new_out):
                os.makedirs(new_out)
            copy_voiceover_files(voices_loc, val, new_out, depth + 1)
    else:
        for file in input:
            shutil.copy(voices_loc + '/' + file, output_dir)
            print(f"{output_dir}\t => {file}")


def main():
    parser = argparse.ArgumentParser(description="Categorize voiceovers of Scrapland characters.")

    parser.add_argument(
        "voices_location",
        metavar="<Voices Location>",
        type=str,
        help="Location where voiceover files are stored. Can be .packed file or folder where files was extracted from .packed",
    )

    parser.add_argument(
        "output_dir",
        metavar="<Ouput Directory>",
        type=str,
        default=".",
        help="Directory where to output categorized voicesover files",
    )

    options = parser.parse_args()

    voices_loc = os.path.abspath(options.voices_loc)
    output_dir = os.path.abspath(options.output_dir)

    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    if voices_loc.endswith(".packed"):
        extract_voiceover_files(voices_loc, voices, output_dir)
    else:
        copy_voiceover_files(voices_loc, voices, output_dir)

    pass


if __name__ == "__main__":
    main()
